        professors = self.generate_professors(
            num_professors,
            num_positive=num_positive,
            positive_weight=positive_weight,
            research_assistant_weight=research_assistant_weight,
            no_spots_weight=no_spots_weight,
            no_response_weight=no_response_weight
        )
        receiver_config = self.generate_receiver_config(receiver_idx)
        emails_backup = self.generate_emails_backup(
            professors, 
            receiver_config["email"], 
            structure_key,
            assign_different_structures=assign_different_structures
        )
        
        # 找到有积极回复的导师（可能有多个）
        positive_professors = [p for p in professors if p["response_type"] == "positive"]
        
        # 收集每个 positive professor 使用的文件结构
        structure_info = {}
        if assign_different_structures and len(positive_professors) > 1:
            for prof in positive_professors:
                assigned_struct = prof.get("assigned_structure", structure_key)
                structure_info[prof["email"]] = {
                    "structure_key": assigned_struct,
                    "structure_info": self.FILE_STRUCTURES.get(assigned_struct, self.FILE_STRUCTURES["standard"])
                }
        else:
            structure_info["default"] = {
                "structure_key": structure_key,
                "structure_info": self.FILE_STRUCTURES.get(structure_key, self.FILE_STRUCTURES["standard"])
            }
        
        return {
            "professors": professors,
            "receiver": receiver_config,
            "emails_backup": emails_backup,
            "file_structure": structure_key,
            "assign_different_structures": assign_different_structures,
            "structure_info": structure_info,
            "positive_professors": positive_professors,
            "weights": {
                "positive": positive_weight,
                "research_assistant": research_assistant_weight,
                "no_spots": no_spots_weight,
                "no_response": no_response_weight
            }
        }


